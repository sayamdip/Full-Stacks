// Number Constants

var PI=Math.PI;
var Euler=Math.E;
var Logarithm2=Math.LN2;

// Rounding Methods
var a=Math.ceil(5.4); // Rounds Up To The Closest Integer
var b=Math.floor(5.4); // Rounds Down To The Closest Integer
var o=Math.round(5.4); // Rounds Up To The Closest Integer If The Decimal Is .5 Or Above; Otherwise, Rounds Down To The Closest Integer
var p=Math.trunc(5.4) // Trims The Decimal, Leaving Only The Integer

// Arithmetic And Calculus Methods
var pow=Math.pow(2,3);
var sqrt=Math.sqrt(16);
var cbrt=Math.cbrt(8);
var abs=Math.abs(-10);

var min=Math.min(9,8,7);
var max=Math.max(9,8,7);

var log=Math.log(8);
var log2=Math.log2(8);
var log10=Math.log10(100);

// Print It
console.log("PI= ",PI);
console.log("Euler= ",Euler);
console.log("Logarithm2= ",Logarithm2);

console.log("Math.ceil(5.4)= ",a);
console.log("Math.floor(5.4)= ",b);
console.log("Math.round(5.4)= ",o);
console.log("Math.truncate(5.4)= ",p);

console.log("Math.pow(2,3)= ",pow);
console.log("Math.sqrt(16)= ",sqrt);
console.log("Math.cbrt(8)= ",cbrt);
console.log("Math.abs(-10)= ",abs);

console.log("Math.min(9,8,7)= ",min);
console.log("Math.max(9,8,7)= ",max);

console.log("Math.log(8)= ",log);
console.log("Math.log2(8)= ",log2);
console.log("Math.log10(100)= ",log10);