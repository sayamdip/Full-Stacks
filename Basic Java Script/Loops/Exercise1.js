for(let i=1;i<=10;i++){
    if(i==1){
        console.log("Gold medal");
    }
    else if(i==2){
        console.log("Silver medal");
    }
    else if(i==3){
        console.log("Bronze medal");
    }
    else{
        console.log(i);
    }
}

for (let j=1;j<=10;j++){
    switch(j){
        case 1:
            console.log("Gold medal");
            break;
        case 2:
            console.log("Silver medal");
            break;
        case 3:
            console.log("Bronze medal");
            break;
        default:
            console.log(j);            

    }
}